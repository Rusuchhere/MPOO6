
package controlador;

/**
 *Clase que suma números
 * @author josed 
 */
public class Sumadora {
    
    /** 
    *Constructor vacío
    */
    public Sumadora() {
    }
   /**MÉTODO QUE REALIZA UNA SUMA DE DOS NÚMEROS
   *@param a El primer numero a sumar 
   *@param b El segundo numero a sumar 
   *@return 
    */
    public double sumar (double a, double b){
        double suma = a+b;
        return suma;
    }
    
}
